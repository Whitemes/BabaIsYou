package baba.is.you.model;

/**
 * The Operator enum represents the various operators used in "BABA IS YOU" to form gameplay rules.
 * Operators define logical relationships between nouns and properties, influencing how game elements interact.
 */
public enum Operator {
    /**
     * The IS operator, pivotal in the game, forms the core of rule creation by linking nouns to properties or other nouns.
     * For example, "BABA IS YOU" allows the player to control BABA, and "WALL IS STOP" makes walls impassable.
     */
    IS;
}
